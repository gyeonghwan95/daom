/** 지역 별칭 (검색어·인덱스 region 보강) */
export const REGION_ALIASES: Record<string, string[]> = {
  센텀: ["센텀시티", "센텀", "해운대"],
  센텀시티: ["센텀", "해운대"],
  재송: ["재송동"],
  반여: ["반여동"],
  해운대: ["해운대구", "센텀"],
  서면: ["부산진구", "부전동"],
  명지: ["강서구"],
  정관: ["기장군"],
};

export function collectAliasesForPath(path: string, slug: string): string[] {
  const haystack = `${path}/${slug}`;
  const aliases = new Set<string>();

  for (const rule of PATH_ALIAS_RULES) {
    if (rule.match.test(haystack)) {
      for (const alias of rule.aliases) {
        aliases.add(alias);
      }
    }
  }

  for (const [key, values] of Object.entries(REGION_ALIASES)) {
    if (haystack.includes(key)) {
      for (const value of values) aliases.add(value);
      aliases.add(key);
    }
  }

  return [...aliases];
}
