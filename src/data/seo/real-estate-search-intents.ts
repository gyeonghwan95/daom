export function getRealEstateIntentsByAction(action: RealEstateIntentAction) {
  return realEstateSearchIntents.filter((item) => item.action === action);
}

export function getRealEstatePriority1CreateNew() {
  return realEstateSearchIntents.filter(
    (item) => item.priority === 1 && item.action === "create-new",
  );
}

export function getRealEstateCreateNewIntents() {
  return realEstateSearchIntents.filter((item) => item.action === "create-new");
}

export function summarizeRealEstateIntentCoverage() {
  const byAction = (action: RealEstateIntentAction) =>
    getRealEstateIntentsByAction(action).map((i) => ({
      keyword: i.primaryKeyword,
      url: i.targetUrl,
    }));

  return {
    strengthenExisting: byAction("strengthen-existing"),
    createNew: byAction("create-new"),
    mergeSection: byAction("merge-section"),
    faqOnly: byAction("faq-only"),
    toolOnly: byAction("tool-only"),
    doNotCreate: byAction("do-not-create"),
  };
}
